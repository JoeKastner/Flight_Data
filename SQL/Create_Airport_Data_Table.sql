CREATE TABLE AIRPORT_DATA(
   AIRPORT_ID serial PRIMARY KEY,
   AIRPORT_NAME VARCHAR (50) UNIQUE NOT NULL,
   CITY VARCHAR (50),
   COUNTRY VARCHAR (50),
   IATA VARCHAR (50),
   ICAO VARCHAR (50),
   LATITUDE VARCHAR (50),
   LONGITUDE VARCHAR (50),
   ALTITUDE VARCHAR (50),
   TIMEZONE VARCHAR (50),
   DST VARCHAR (50),
   TZ_DB VARCHAR (50),
   AIRPORT_TYPE VARCHAR (50),
   DATA_SOURCE VARCHAR (50));